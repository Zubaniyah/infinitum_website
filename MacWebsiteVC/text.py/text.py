def remove_integers(s):
    lines = s.split('\n')
    output = []

    for line in lines:
        stripped_line = line.strip()
        # We use the isdigit() method to check if the line is an integer
        # and we also need to make sure that the line is not empty
        # (as the empty string would also return False for isdigit()).
        if not (stripped_line.isdigit() and stripped_line):
            output.append(line)

    return '\n'.join(output)

